        private ImmutableArray<ElementPlan> BuildElementPlans(AkburaDocumentSyntax document)
        {
            var roots = ImmutableArray.CreateBuilder<ElementPlan>();
            foreach (var root in document.Members.OfType<MarkupRootSyntax>())
            {
