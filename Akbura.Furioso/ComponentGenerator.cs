        private static string GetCommandType(ICommandSymbol command)
        {
            if (command.Parameters.IsEmpty)
            {
                return "global::Akbura.IAkburaCommand";
            }

            var result = new StringBuilder("global::Akbura.IAkburaCommand<");
            foreach (var parameter in command.Parameters)
            {
                result.Append(GetTypeName(parameter.Type.Symbol)).Append(", ");
            }

            result.Append(GetTypeName(command.ResultType.Symbol)).Append('>');
            return result.ToString();
        }

