    private static string? GetStaticMemberReference(Microsoft.CodeAnalysis.ISymbol? symbol)
    {
        return symbol switch
        {
            RoslynFieldSymbol { IsStatic: true } field =>
                $"{GetTypeName(field.ContainingType)}.{EscapeIdentifier(field.Name)}",
            RoslynPropertySymbol { IsStatic: true } property =>
                $"{GetTypeName(property.ContainingType)}.{EscapeIdentifier(property.Name)}",
            _ => null,
        };
    }

    private static string GetMethodReference(RoslynMethodSymbol method)
    {
        return $"{GetTypeName(method.ContainingType)}.{EscapeIdentifier(method.Name)}";
    }

    private static string GetTypeName(Microsoft.CodeAnalysis.ISymbol? symbol)
    {
        return symbol is ITypeSymbol type
            ? type.ToDisplayString(SymbolDisplayFormat.FullyQualifiedFormat)
            : "global::System.Object";
    }

    private static string FormatConstant(object? value, Microsoft.CodeAnalysis.ISymbol? targetType)
    {
        if (value == null)
        {
            return "null";
        }

        if (targetType is INamedTypeSymbol { TypeKind: TypeKind.Enum } enumType)
        {
            return $"({GetTypeName(enumType)}){Convert.ToInt64(value, CultureInfo.InvariantCulture).ToString(CultureInfo.InvariantCulture)}";
        }

        return value switch
        {
            string text => ToStringLiteral(text),
            char character => SymbolDisplay.FormatLiteral(character, quote: true),
            bool boolean => boolean ? "true" : "false",
            byte number => number.ToString(CultureInfo.InvariantCulture),
            sbyte number => number.ToString(CultureInfo.InvariantCulture),
            short number => number.ToString(CultureInfo.InvariantCulture),
            ushort number => number.ToString(CultureInfo.InvariantCulture),
            int number => number.ToString(CultureInfo.InvariantCulture),
            uint number => number.ToString(CultureInfo.InvariantCulture) + "U",
            long number => number.ToString(CultureInfo.InvariantCulture) + "L",
            ulong number => number.ToString(CultureInfo.InvariantCulture) + "UL",
            float number => number.ToString("R", CultureInfo.InvariantCulture) + "F",
            double number => FormatDouble(number),
            decimal number => number.ToString(CultureInfo.InvariantCulture) + "M",
            _ => ToStringLiteral(value.ToString() ?? string.Empty),
        };
    }

    private static string FormatDouble(double value)
    {
        if (double.IsNaN(value))
        {
            return "global::System.Double.NaN";
        }

        if (double.IsPositiveInfinity(value))
        {
            return "global::System.Double.PositiveInfinity";
        }

        if (double.IsNegativeInfinity(value))
        {
            return "global::System.Double.NegativeInfinity";
        }

        return value.ToString("R", CultureInfo.InvariantCulture);
    }

    private static string EscapeInterpolatedText(string text)
    {
        return text
            .Replace("\\", "\\\\")
            .Replace("\"", "\\\"")
            .Replace("{", "{{")
            .Replace("}", "}}");
    }

    private static string EscapeIdentifier(string name)
    {
        return CSharpSyntaxFacts.GetKeywordKind(name) != CSharpSyntaxKind.None ||
               CSharpSyntaxFacts.GetContextualKeywordKind(name) != CSharpSyntaxKind.None
            ? "@" + name
            : name;
    }

    private static string SanitizeIdentifier(string value)
    {
        var result = new StringBuilder(value.Length);
        foreach (var character in value)
        {
            result.Append(char.IsLetterOrDigit(character) || character == '_'
                ? character
                : '_');
        }

        return result.Length == 0 ? "value" : result.ToString();
    }

    private static string SanitizeHintPart(string value)
    {
        var result = new StringBuilder(Math.Min(value.Length, 64));
        foreach (var character in value)
        {
            if (result.Length == 64)
            {
                break;
            }

            result.Append(char.IsLetterOrDigit(character) || character is '.' or '_'
                ? character
                : '_');
        }

        return result.Length == 0 ? "component" : result.ToString();
    }

    private static string ToStringLiteral(string value)
    {
        return SymbolDisplay.FormatLiteral(value, quote: true);
    }

    private static string ToLineDirectivePath(string path)
    {
        return "\"" + path + "\"";
    }

    private static StringBuilder AppendIndented(StringBuilder source, int indentation)
    {
        return source.Append(' ', indentation * 4);
    }

    private static void AppendIndentedLine(StringBuilder source, int indentation, string text)
    {
        source.Append(' ', indentation * 4).AppendLine(text);
    }
