        private string GetPropertyValue(IMarkupPropertySetterOperation operation)
        {
            if (operation.ValueKind == MarkupAttributeValueKind.DynamicExpression &&
                operation.ValueSyntax is MarkupDynamicAttributeValueSyntax dynamicValue)
            {
                return dynamicValue.Expression.Expression.GetRawCSharpExpression()?.ToFullString().Trim() ?? "default";
            }

            if (operation.ConvertedValue is MarkupExtensionValue extension)
            {
                return GetMarkupExtensionExpression(extension);
            }

            if (operation.ConvertedValue is GridDefinitionListValue definitions)
            {
                return GetGridDefinitionsExpression(definitions, operation.Property?.Type.Symbol);
            }

            if (operation.ConvertedValue is MarkupLiteralValue convertedLiteral)
            {
                return GetConvertedLiteralExpression(convertedLiteral);
            }

            if (operation.ConvertedValue is CSharpSymbolDefinition definition &&
                GetStaticMemberReference(definition.Symbol) is { } member)
            {
                return member;
            }

            if (operation.ConvertedValue != null)
            {
                return FormatConstant(operation.ConvertedValue, operation.Property?.Type.Symbol);
            }

            if (operation.LiteralValue != null)
            {
                return ToStringLiteral(operation.LiteralValue);
            }

            return operation.ValueOperation.Syntax?.ToString() ?? "default";
        }

        private static string GetConvertedLiteralExpression(MarkupLiteralValue literal)
        {
            var type = GetTypeName(literal.TargetType.Symbol);
            var text = ToStringLiteral(literal.Text);
            return literal.ConverterKind switch
            {
                MarkupLiteralConverterKind.ParseMethod when literal.Converter.Symbol is RoslynMethodSymbol method =>
                    $"{GetMethodReference(method)}({text})",
                MarkupLiteralConverterKind.StringConstructor => $"new {type}({text})",
                MarkupLiteralConverterKind.TypeConverter when literal.Converter.Symbol is INamedTypeSymbol converterType =>
                    $"({type})new {GetTypeName(converterType)}().ConvertFromInvariantString({text})!",
                _ => $"({type}){text}",
            };
        }

        private string GetMarkupExtensionExpression(MarkupExtensionValue extension)
        {
            if (extension.Binding is { } binding)
            {
                return GetBindingExpression(extension, binding);
            }

            var type = GetTypeName(extension.ExtensionType.Symbol);
            var arguments = string.Join(", ", extension.Arguments.Select(GetMarkupExtensionArgument));
            var creation = new StringBuilder("new ").Append(type).Append('(').Append(arguments).Append(')');
            if (!extension.Properties.IsEmpty)
            {
                creation.Append(" { ");
                for (var index = 0; index < extension.Properties.Length; index++)
                {
                    if (index > 0)
                    {
                        creation.Append(", ");
                    }

                    var property = extension.Properties[index];
                    creation.Append(EscapeIdentifier(property.Name)).Append(" = ")
                        .Append(GetMarkupExtensionProperty(property));
                }

                creation.Append(" }");
            }

            if (extension.ProvideValueMethod.Symbol is RoslynMethodSymbol provideValue)
            {
                creation.Insert(0, '(').Append(')');
                creation.Append('.').Append(EscapeIdentifier(provideValue.Name)).Append('(');
                if (provideValue.Parameters.Length == 1)
                {
                    creation.Append("null!");
                }

                creation.Append(')');
            }

            return creation.ToString();
        }

        private string GetBindingExpression(MarkupExtensionValue extension, MarkupBindingValue binding)
        {
            if (binding.Kind == MarkupBindingKind.Compiled &&
                TryGetCompiledBindingPathExpression(binding, out var pathExpression))
            {
                var sourceType = GetTypeName(binding.SourceType.Symbol);
                var resultType = GetTypeName(binding.ResultType.Symbol);
                var compiledResult = new StringBuilder("global::Avalonia.Data.CompiledBinding.Create<")
                    .Append(sourceType).Append(", ").Append(resultType)
                    .Append(">(static __source => ").Append(pathExpression);
                AppendCompiledBindingArguments(compiledResult, extension.Properties);
                return compiledResult.Append(')').ToString();
            }

            var type = GetTypeName(binding.BindingType.Symbol);
            var result = new StringBuilder("new ").Append(type).Append('(')
                .Append(ToStringLiteral(binding.Path)).Append(')');
            if (!extension.Properties.IsEmpty)
            {
                result.Append(" { ");
                for (var index = 0; index < extension.Properties.Length; index++)
                {
                    if (index > 0)
                    {
                        result.Append(", ");
                    }

                    var property = extension.Properties[index];
                    result.Append(EscapeIdentifier(property.Name)).Append(" = ")
                        .Append(GetMarkupExtensionProperty(property));
                }

                result.Append(" }");
            }

            return result.ToString();
        }

        private static bool TryGetCompiledBindingPathExpression(
            MarkupBindingValue binding,
            out string expression)
        {
            var result = new StringBuilder("__source");
            var pendingLogicalNot = 0;
            foreach (var element in binding.PathElements)
            {
                switch (element.Kind)
                {
                    case MarkupBindingPathElementKind.Property:
                    case MarkupBindingPathElementKind.Field:
                        result.Append('.').Append(EscapeIdentifier(element.Symbol.Name ?? element.Text));
                        break;
                    case MarkupBindingPathElementKind.Indexer:
                        result.Append('[');
                        var argumentCount = element.BoundArguments.IsEmpty
                            ? element.Arguments.Length
                            : element.BoundArguments.Length;
                        for (var index = 0; index < argumentCount; index++)
                        {
                            if (index > 0)
                            {
                                result.Append(", ");
                            }

                            if (element.BoundArguments.IsEmpty)
                            {
                                result.Append(element.Arguments[index]);
                            }
                            else
                            {
                                var argument = element.BoundArguments[index];
                                result.Append(!argument.Operation.IsDefault
                                    ? argument.Operation.Syntax?.ToString() ?? argument.Text
                                    : argument.ConvertedValue != null
                                        ? FormatConstant(argument.ConvertedValue, argument.Type.Symbol)
                                        : argument.Text);
                            }
                        }

                        result.Append(']');
                        break;
                    case MarkupBindingPathElementKind.TypeCast when element.Type.Symbol != null:
                        result.Insert(0, "((" + GetTypeName(element.Type.Symbol) + ")").Append(')');
                        break;
                    case MarkupBindingPathElementKind.Not:
                        pendingLogicalNot++;
                        break;
                    default:
                        expression = string.Empty;
                        return false;
                }
            }

            if (pendingLogicalNot > 0)
            {
                result.Insert(0, new string('!', pendingLogicalNot));
            }

            expression = result.ToString();
            return true;
        }

        private void AppendCompiledBindingArguments(
            StringBuilder result,
            ImmutableArray<MarkupExtensionPropertyValue> properties)
        {
            foreach (var property in properties)
            {
                var parameterName = property.Name switch
                {
                    "Converter" => "converter",
                    "ConverterCulture" => "converterCulture",
                    "ConverterParameter" => "converterParameter",
                    "Delay" => "delay",
                    "FallbackValue" => "fallbackValue",
                    "Mode" => "mode",
                    "Priority" => "priority",
                    "Source" => "source",
                    "StringFormat" => "stringFormat",
                    "TargetNullValue" => "targetNullValue",
                    "UpdateSourceTrigger" => "updateSourceTrigger",
                    _ => null,
                };
                if (parameterName != null)
                {
                    result.Append(", ").Append(parameterName).Append(": ")
                        .Append(GetMarkupExtensionProperty(property));
                }
            }
        }

        private string GetMarkupExtensionArgument(MarkupExtensionArgumentValue argument)
        {
            if (argument.NestedValue != null)
            {
                return GetMarkupExtensionExpression(argument.NestedValue);
            }

            if (!argument.Operation.IsDefault)
            {
                return argument.Operation.Syntax?.ToString() ?? argument.Text;
            }

            return argument.ConvertedValue != null
                ? FormatConstant(argument.ConvertedValue, argument.Type.Symbol)
                : ToStringLiteral(argument.Text.Trim('\'', '"'));
        }

        private string GetMarkupExtensionProperty(MarkupExtensionPropertyValue property)
        {
            if (property.NestedValue != null)
            {
                return GetMarkupExtensionExpression(property.NestedValue);
            }

            if (property.ConvertedValue != null)
            {
                return FormatConstant(property.ConvertedValue, property.Type.Symbol);
            }

            if (!property.Operation.IsDefault)
            {
                return property.Operation.Syntax?.ToString() ?? property.Value;
            }

            return ToStringLiteral(property.Value.Trim('\'', '"'));
        }

        private static string GetGridDefinitionsExpression(
            GridDefinitionListValue value,
            Microsoft.CodeAnalysis.ISymbol? targetType)
        {
            var isRows = targetType?.Name == "RowDefinitions";
            var collectionType = isRows
                ? "global::Avalonia.Controls.RowDefinitions"
                : "global::Avalonia.Controls.ColumnDefinitions";
            var itemType = isRows
                ? "global::Avalonia.Controls.RowDefinition"
                : "global::Avalonia.Controls.ColumnDefinition";
            var lengthProperty = isRows ? "Height" : "Width";
            var minProperty = isRows ? "MinHeight" : "MinWidth";
            var maxProperty = isRows ? "MaxHeight" : "MaxWidth";
            var result = new StringBuilder("new ").Append(collectionType).Append(" { ");
            for (var index = 0; index < value.Definitions.Length; index++)
            {
                if (index > 0)
                {
                    result.Append(", ");
                }

                var definition = value.Definitions[index];
                result.Append("new ").Append(itemType).Append(" { ")
                    .Append(lengthProperty).Append(" = ").Append(GetGridLengthExpression(definition.Length));
                if (definition.Min is { } min)
                {
                    result.Append(", ").Append(minProperty).Append(" = ").Append(FormatDouble(min));
                }

                if (definition.Max is { } max)
                {
                    result.Append(", ").Append(maxProperty).Append(" = ").Append(FormatDouble(max));
                }

                result.Append(" }");
            }

            return result.Append(" }").ToString();
        }

        private static string GetGridLengthExpression(GridDefinitionLengthValue length)
        {
            var unit = length.UnitType switch
            {
                GridDefinitionUnitType.Auto => "global::Avalonia.Controls.GridUnitType.Auto",
                GridDefinitionUnitType.Star => "global::Avalonia.Controls.GridUnitType.Star",
                _ => "global::Avalonia.Controls.GridUnitType.Pixel",
            };
            return $"new global::Avalonia.Controls.GridLength({FormatDouble(length.Value)}, {unit})";
        }

        private string? GetContentExpression(IMarkupContentOperation operation)
        {
            if (!operation.IsSynthesizedString)
            {
                return operation.LiteralValue == null ? null : ToStringLiteral(operation.LiteralValue);
            }

            var result = new StringBuilder("$\"");
            foreach (var content in operation.Syntax.Body)
            {
                switch (content)
                {
                    case MarkupTextLiteralSyntax text:
                        result.Append(EscapeInterpolatedText(text.ToFullString()));
                        break;
                    case MarkupInlineExpressionSyntax expression:
                        result.Append('{').Append(GetExpression(expression.Expression.Expression)).Append('}');
                        break;
                }
            }

            return result.Append('"').ToString();
        }

        private static string GetHandlerExpression(IMarkupRoutedEventBindingOperation operation)
        {
            var expression = operation.ValueSyntax is MarkupDynamicAttributeValueSyntax dynamicValue
                ? dynamicValue.Expression.Expression.GetRawCSharpExpression()?.ToFullString().Trim()
                : null;
            if (string.IsNullOrWhiteSpace(expression))
            {
                return "static (_, _) => { }";
            }

            if (operation.HandlerKind is MarkupCommandHandlerKind.Lambda or MarkupCommandHandlerKind.DirectReference)
            {
                return expression!;
            }

            var parameters = operation.HandlerParameterCount switch
            {
                0 => "()",
                1 => "__argument",
                _ => "(__sender, __args)",
            };
            var asyncPrefix = operation.IsAsync ? "async " : string.Empty;
            return $"{asyncPrefix}{parameters} => {{ {expression}; }}";
        }

        private static bool IsFirstUpdateValue(IMarkupPropertySetterOperation operation)
        {
            return operation.ValueKind is MarkupAttributeValueKind.Literal or MarkupAttributeValueKind.MarkupExtension;
        }

        private static bool IsDataTypeDirective(MarkupAttributeSyntax syntax)
        {
            return syntax is MarkupAttachedPropertyAttributeSyntax attached &&
                   attached.ToFullString().TrimStart().StartsWith("x.DataType", StringComparison.Ordinal);
        }

        private void AppendLineDirective(
            StringBuilder source,
            int indentation,
            AkburaSyntax syntax,
            string statement,
            int valueOffset)
        {
            if (_sourceMap.TryGetLineDirective(syntax, out var span, out var path))
            {
                var generatedOffset = indentation * 4 + Math.Max(0, valueOffset);
                AppendIndented(source, indentation)
                    .Append("#line (").Append(span.Start.Line + 1).Append(',').Append(span.Start.Character + 1)
                    .Append(")-(").Append(span.End.Line + 1).Append(',').Append(span.End.Character + 1)
                    .Append(") ").Append(generatedOffset).Append(' ').Append(ToLineDirectivePath(path)).AppendLine();
                AppendIndentedLine(source, indentation, statement);
                AppendIndentedLine(source, indentation, "#line default");
                return;
            }

            AppendIndentedLine(source, indentation, statement);
        }

